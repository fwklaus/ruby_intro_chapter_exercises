# What is exception handling and what problem does it solve?

=begin
Exception handling is a built-in way for us to debug our code, to anticipate
errors the program will raise when it runs and tell the program what to 
do when it meets them. It allows us to isolate errors, and run programs
normally without exiting like they normally would when encountering an error
=end

# Launch Answer
=begin
Exception handling is a structure used to handle the possibility of an error 
